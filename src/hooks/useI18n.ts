export function useI18n() {
  /**
   * Detects the best language to use at startup.
   *
   * Priority order:
   * 1. `localStorage["trix_lang"]` — user's explicit selection.
   * 2. Exact match against `navigator.language` (e.g. `"pt-BR"`).
   * 3. Base-language prefix match (e.g. `"pt"` matches `"pt-BR"`).
   * 4. Falls back to `"en"` if nothing matches.
   */
  function detectLanguage(): string {
    try {
      const saved = localStorage.getItem('trix_lang');
      if (saved) return saved;
    } catch { /* localStorage unavailable */ }
    const navLang = navigator.language;
    if (translations[navLang]) return navLang;
    const base = navLang.split('-')[0];
    for (const code of Object.keys(translations)) {
      if (code.startsWith(base)) return code;
    }
    return 'en';
  }

  const [language, setLanguage] = useState(detectLanguage);

  // Persist the selected language to localStorage whenever it changes.
  useEffect(() => {
    try {
      localStorage.setItem('trix_lang', language);
    } catch { /* quota exceeded or storage unavailable */ }
  }, [language]);

  // Sync the document direction (RTL for Arabic) and the `lang` attribute
  // so assistive technologies and CSS `[dir="rtl"]` selectors work correctly.
  useEffect(() => {
    const rtlLanguages = ['ar'];
    document.documentElement.dir = rtlLanguages.includes(language) ? 'rtl' : 'ltr';
    document.documentElement.lang = language;
  }, [language]);

  /**
   * Translates a dot-separated key into the active language string.
   * Falls back to English and then to the raw key if neither locale has the key.
   */
  const t = useCallback((key: string) => {
    return translations[language]?.[key] || translations['en']?.[key] || key;
  }, [language]);

  /** `true` when the active language is written right-to-left (currently only Arabic). */
  const isRTL = language === 'ar';
  return { language, setLanguage, t, isRTL };
}
